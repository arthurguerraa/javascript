/* 1- Quais são as três etapas no processo de resolução de problemas?
Entender o problema — saber exatamente o que é pedido.

Planejar — decidir passos para chegar na solução (algoritmo/pseudocódigo).

Executar / dividir e testar — transformar o plano em código, dividir em partes menores e testar cada parte.

Analogia rápida: é como montar um móvel — primeiro você lê o manual (entender), depois separa as peças e vê a ordem de montagem (planejar), por fim monta parte por parte e confere se está firme (executar/testar).*/


/* 2- Por que é importante entender claramente o problema primeiro?
Se você não entendeu direito, pode fazer a coisa errada mesmo trabalhando muito. Entender evita desperdício de tempo e garante que a solução resolva exatamente o que foi pedido.

Exemplo: pedir “faça um botão” vs “faça um botão que envia um formulário” — soluções diferentes.*/ 


/* 3- O que você pode fazer para ajudar a obter um entendimento mais claro do problema?
Leia o enunciado com calma e reescreva com suas próprias palavras.

Pergunte (ou imagine) qual é a entrada e qual é a saída esperada.

Desenhe ou escreva exemplos (input → output).

Simplifique o problema: resolva um caso fácil primeiro.

Pequeno exercício mental: transforme “ordenar nomes” em “ordenar ['ana','bruno','carlos'] → ['ana','bruno','carlos']”.*/


/* 4- O que você deve fazer na etapa de planejamento do processo de resolução de problemas?
Liste os passos necessários (um por vez).

Identifique subproblemas (o que pode ser separado).

Escolha estruturas (loop, condição, funções) que vai usar.

Escreva pseudocódigo ou um rascunho antes de codar.

Pense em casos limites (o que acontece se a entrada for vazia?).

Exemplo de perguntas no planejamento: “De onde vem o dado?”, “O que faço se o usuário não digitar nada?”, “Posso quebrar isso em 2 funções?”.*/


/* 5- O que é um algoritmo?
Um algoritmo é só uma sequência de passos claros que resolve um problema — tipo uma receita de bolo, mas para o computador.

Exemplo curto (algoritmo para ligar um computador): ligar na tomada → apertar botão → esperar carregar → fazer login.*/


/* 6- O que é pseudocódigo?
Pseudocódigo é escrever o algoritmo em linguagem simples, nem tudo formal como linguagem de programação. Serve pra planejar sem se preocupar com sintaxe.

Exemplo (pseudocódigo para somar números até N):

Receber N do usuário
Para i de 1 até N:
  somar i em total
Mostrar total
*/


/* 7) Quais são as vantagens de dividir um problema e resolver problemas menores?
Fica mais fácil entender e codar cada parte.

É mais rápido achar e consertar erros (testa pedaço por pedaço).

Você pode reutilizar partes em outros problemas.

Menos estresse — dá sensação de progresso (resolver uma parte já é vitória).

Analogia: em vez de montar uma casa inteira de uma vez, construir cômodo por cômodo.*/